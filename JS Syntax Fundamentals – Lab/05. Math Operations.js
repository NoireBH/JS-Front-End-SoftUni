function solve(num1,num2,string){
switch (string) {
    case '+':
        console.log(num1 + num2);
        break;
        case '+':
        console.log(num1 + num2);
        break;
        case '-':
        console.log(num1 - num2);
        break;
        case '*':
        console.log(num1 * num2);
        break;
        case '/':
        console.log(num1 / num2);
        break;
        case '**':
        console.log(num1 ** num2);
        break;
        case '%':
        console.log(num1 % num2);
        break;

    default:
        break;
}
}

solve(5,5,'*');